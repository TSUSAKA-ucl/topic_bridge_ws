#!/usr/bin/bash
cd $(dirname `readlink -f "$0"`)
ThisDir=`pwd -P`
[ ! -e "$ThisDir"/js ] && mkdir "$ThisDir"/js
if [ -d "$ThisDir"/js ]
then curl -o js/msgpack.min.js \
	  https://unpkg.com/@msgpack/msgpack@3.1.2/dist.umd/msgpack.min.js
else echo cannot make the directory "$ThisDir"/js
     exit 1
fi
